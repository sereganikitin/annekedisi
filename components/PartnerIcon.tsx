export function detectPlatform(url: string): Platform {
  if (!url) return "web";
  const trimmed = url.trim();
  if (trimmed.startsWith("mailto:")) return "email";
  if (trimmed.startsWith("tel:")) return "phone";
  if (trimmed.endsWith(".xml") && /rss|atom|feed/i.test(trimmed)) return "rss";

  try {
    const u = new URL(trimmed, "https://_");
    const hostAndPath = (u.host + u.pathname).replace(/\/+$/, "").toLowerCase();
    for (const [p, re] of PATTERNS) {
      if (re.test(u.host) || re.test(hostAndPath)) return p;
    }
  } catch {
    /* fall through */
  }
  return "web";
}

